#include "cube.h"
#include <cmath>
#include <iostream>
void cube(
  Eigen::MatrixXd & V,
  Eigen::MatrixXi & F,
  Eigen::MatrixXd & UV,
  Eigen::MatrixXi & UF,
  Eigen::MatrixXd & NV,
  Eigen::MatrixXi & NF)
{
  ////////////////////////////////////////////////////////////////////////////
  // Add your code here:
  ////////////////////////////////////////////////////////////////////////////

  ////Hint:
  V.resize(8,3);
  F.resize(6,4);
  UV.resize(14,2);
  UF.resize(6,4);
  NV.resize(6,3);
  NF.resize(6,4);

  V = Eigen::MatrixXd::Constant(8, 3, 1.0);//(x, y, z)
  for (double i = 0; i < 2; i++) {
    for (double j = 0; j < 2; j++) {
      for (double k = 0; k < 2; k++) {
        int row = i*4 + j*2 + k*1;
        V(row, 0) = i;
        V(row, 1) = j;
        V(row, 2) = k;
      }
    }  
  }

  F << 0, 2, 6, 4, 
       4, 5, 7, 6,
       3, 2, 6, 7, 
       0, 1, 3, 2,
       1, 3, 7, 5,
       0, 1, 5, 4;
    
  UV << 0.00, 0.25, //0
        0.00, 0.50, //1
        0.25, 0.50,//2
        0.25, 0.75,//3
        0.50, 0.75,//4
        0.50, 0.50,//5
        0.75, 0.50,//6
        1.00, 0.50,//7   

        1.00, 0.25,//8
        0.75, 0.25,//9
        0.50, 0.25,//10
        0.50, 0.00, //11
        0.25, 0.00,//12
        0.25, 0.25; //13

    
  UF << 1, 0, 13, 2,
        3, 2, 5, 4,
        2, 13, 10, 5,
        13, 12, 11, 10,
        5, 10, 9, 6,
        6, 9, 8, 7;
    
  NV << 0, 0, -1,
        0, 0, 1,
        0, -1, 0,
        0, 1, 0,
        -1, 0, 0,
        1, 0, 0;
    
    NF << 0, 0, 0, 0,
          5, 5, 5, 5,
          3, 3, 3, 3,
          4, 4, 4, 4,
          1, 1, 1, 1,
          2, 2, 2, 2;


}
